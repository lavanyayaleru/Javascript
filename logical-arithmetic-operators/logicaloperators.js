// In logical-operators we have 3 operators
// 1.lofical and(&&)
// 2.logical Or(||)
// 3.logical Not(!)

// // 1.lofical and(&&):
console.log(true&&true)
console.log(true&&false)
console.log(false&&false)

// // 2.logical Or(||):
console.log(true||false)
console.log(true||true)
console.log(false||false)

// // 3.logical Not(!)
console.log(true)
console.log(!true)
console.log(false)
console.log(!false)


